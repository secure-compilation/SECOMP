       (loadind_ptr_stk f.(fn_link_ofs) SPC k false) false.

Definition make_return (k: asm_code) :=
  let next_label := k.(ac_nextlabel) in

  PCgs X31 RAC >>
  Pbeqw X31 X0 next_label >>
  Pj_r RAC >>
  Plabel next_label >>
  PCinvoke RAC SPC true >>
  incr_label k.
  
